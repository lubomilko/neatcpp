uint32_t Multiply(uint16_t ValA, uint16_t ValB)
{
    uint32_t RetVal;

    /* Perform multiplication. */
    RetVal = (uint32_t)ValA * (uint32_t)ValB;

    return RetVal;
}


int16_t Multiply(int8_t ValA, int8_t ValB)
{
    int16_t RetVal;

    /* Perform multiplication. */
    RetVal = (int16_t)ValA * (int16_t)ValB;

    return RetVal;
}
